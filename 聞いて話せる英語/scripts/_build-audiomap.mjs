// 英語アプリの audioMap.ts を audio/<lang>/{conv,gram} から生成(全同梱)。
//  en=学習対象 / ja=母語。audioMap.ts は expo-app/data/ にあるので相対は ../../audio/...。
//  Metro watchFolders=シリーズroot なのでバンドル可。
import fs from 'node:fs'; import path from 'node:path'; import { fileURLToPath } from 'node:url';
const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const AUDIO = path.join(ROOT, 'audio');
const OUT = path.join(ROOT, 'expo-app/data/audioMap.ts');
function entries(lang, mode) {
  const dir = path.join(AUDIO, lang, mode);
  if (!fs.existsSync(dir)) return '';
  return fs.readdirSync(dir).filter(f => f.endsWith('.mp3')).sort()
    .map(f => `  '${f.replace(/\.mp3$/, '')}': require('../../audio/${lang}/${mode}/${f}'),`).join('\n');
}
const enConv = entries('en', 'conv'), enGram = entries('en', 'gram');
const jaConv = entries('ja', 'conv'), jaGram = entries('ja', 'gram');
const ts = `// AUTO-GENERATED by scripts/_build-audiomap.mjs. Do not edit by hand.
// 英語アプリ音声(全同梱)。en=学習対象 / ja=母語。audio/<lang>/{conv,gram}。
export const englishAudio: Record<string, number> = {
${enConv}
};
export const englishGrammarAudio: Record<string, number> = {
${enGram}
};
export const japaneseNativeAudio: Record<string, number> = {
${jaConv}
};
export const japaneseNativeGrammarAudio: Record<string, number> = {
${jaGram}
};
`;
fs.writeFileSync(OUT, ts);
const n = s => s ? s.split('\n').filter(Boolean).length : 0;
console.log(`audioMap.ts: en conv ${n(enConv)}/gram ${n(enGram)} / ja conv ${n(jaConv)}/gram ${n(jaGram)}`);
