function applySheet(s, rows) {
  rows.forEach((r, i) => {
    const row = s.getRow(i + 2);
    row.getCell(1).value = r[0]; row.getCell(2).value = r[3];
    row.getCell(3).value = r[1]; row.getCell(4).value = r[4];
    row.getCell(5).value = r[2]; row.getCell(6).value = r[5];
    row.commit?.();
  });
}

