// audioMap.ts を「ネパール語音声のみ」で再生成する (App A 全DL化)。
// ne=コア同梱。ja/en は DLパックで file:// 提供するため同梱不要。
// 入力: expo-app/assets/audio/nepali/*.mp3, nepali-grammar/*.mp3
// 出力: expo-app/data/audioMap.ts
// 実行: node scripts/build-audiomap.mjs
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const APP = path.join(ROOT, 'expo-app');
const AUDIO = path.join(APP, 'assets', 'audio');
const OUT = path.join(APP, 'data', 'audioMap.ts');

function entries(dir, rel) {
  const files = fs.readdirSync(path.join(AUDIO, dir)).filter(f => f.endsWith('.mp3'));
  files.sort();
  return files.map(f => `  '${f.replace(/\.mp3$/, '')}': require('../assets/audio/${rel}/${f}'),`).join('\n');
}

const conv = entries('nepali', 'nepali');
const gram = entries('nepali-grammar', 'nepali-grammar');

const ts = `// AUTO-GENERATED by scripts/build-audiomap.mjs. Do not edit by hand.
// ネパール語音声のみ同梱 (母語 ja/en は DL パックで file:// 提供)。

export const nepaliAudio: Record<string, number> = {
${conv}
};

export const nepaliGrammarAudio: Record<string, number> = {
${gram}
};
`;

fs.writeFileSync(OUT, ts);
const n = (s) => (s ? s.split('\n').length : 0);
console.log(`audioMap.ts: nepaliAudio ${n(conv)} / nepaliGrammarAudio ${n(gram)}`);
