async function main() {
  const wb = new ExcelJS.Workbook();
  await wb.xlsx.readFile(XLSX);

  for (const { sheet, items } of SHEETS) {
    // 同名シートがあれば削除して作り直す（冪等にするため）
    const existing = wb.getWorksheet(sheet);
    if (existing) wb.removeWorksheet(existing.id);
    const ws = wb.addWorksheet(sheet);
    ws.getCell('A1').value = '日本語';
    ws.getCell('B1').value = 'ネパール語';
    ws.getRow(1).font = { bold: true };
    items.forEach(([ja, ne], i) => {
      ws.getCell(`A${i + 2}`).value = ja;
      ws.getCell(`B${i + 2}`).value = ne;
    });
    ws.getColumn('A').width = 36;
    ws.getColumn('B').width = 48;
    console.log(`  ${sheet}: ${items.length} 例`);
  }

  await wb.xlsx.writeFile(XLSX);
  console.log(`\n書き込み完了: ${XLSX}`);
}

main().catch(err => { console.error(err); process.exit(1); });
