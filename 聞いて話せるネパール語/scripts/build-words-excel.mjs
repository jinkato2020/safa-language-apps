function sanitizeSheetName(name) {
  return name.replace(/[\\/?*[\]:]/g, '_').slice(0, 31);
}

async function main() {
  const wb = new ExcelJS.Workbook();
  let totalWords = 0;

  for (const cat of CATEGORIES) {
    const sheetName = sanitizeSheetName(cat.name);
    const sheet = wb.addWorksheet(sheetName);

    const header = sheet.getRow(1);
    header.getCell(1).value = '日本語';
    header.getCell(2).value = 'ネパール語';
    header.font = { bold: true };
    header.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FFFFE4E1' } };
    header.alignment = { vertical: 'middle', horizontal: 'center' };

    cat.words.forEach((row, i) => {
      const r = sheet.getRow(i + 2);
      r.getCell(1).value = row[0];
      r.getCell(2).value = row[1];
      r.alignment = { vertical: 'top', wrapText: true };
    });

    sheet.getColumn(1).width = 28;
    sheet.getColumn(2).width = 36;
    header.height = 22;
    sheet.views = [{ state: 'frozen', ySplit: 1 }];

    totalWords += cat.words.length;
    console.log(`  ${sheetName}: ${cat.words.length}語`);
  }

  await wb.xlsx.writeFile(OUTPUT_PATH);
  console.log('');
  console.log('========================================');
  console.log(`カテゴリー数: ${CATEGORIES.length}`);
  console.log(`総単語数: ${totalWords}`);
  console.log(`出力: ${OUTPUT_PATH}`);
}

main().catch(e => { console.error(e); process.exit(1); });
